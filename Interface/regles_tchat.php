        <h1>Règles générales pour l’utilisation du tchat</h1>

        <p>Notre système de tchat a été développé afin d’offrir une plateforme de communication à tous les joueurs.</br>
          N’oubliez jamais qu’une autre personne se trouve de l’autre côté du tchat, veuillez toujours tchater de manière aimable et respectueuse.</br></br>
        </p>

        <h3>Remarque pour les mineurs et leurs parents</h3>

        <p>Nous nous réjouissons que nos jeux vous plaisent et que vous souhaitiez vous entretenir avec d’autres joueurs.</br>
          Si un joueur vous écrit des choses vous mettant mal à l’aise ou si vous voyez qu’un joueur ne respecte pas les règles énoncées ici, tournez-vous vers les modérateurs du tchat, vers vos parents ou contactez le support du jeu.</br>
          N’envoyez jamais de photos de vous et faites attention à ce que vous dites aux autres joueurs sur vous-même.</br>
          Vous n’êtes pas obligé de répondre à toutes les questions ou de continuez une discussion vous mettant mal à l’aise.</br></br>
        </p>

        <h3>Informations personnelles</h3>

        <p>Pour votre propre sécurité, il est formellement interdit de poster sur le tchat des données personnelles, que ce soit les vôtres ou celles d’autres joueurs.</br>
          Ne donnez jamais votre vrai nom, votre âge, votre adresse postale ou votre adresse e-mail, votre adresse messenger, votre numéro de téléphone, et n’indiquez pas l’école à laquelle vous allez.</br>
          Ne demandez pas non plus ces informations à d’autres joueurs.</br>
          N’acceptez jamais de rencontrer en vrai et seul une personne avec qui vous avez tchaté. On ne peut jamais savoir qui est vraiment cette personne et quelles sont ses intentions.</br></br>
        </p>

        <h3>Les gênes à éviter sur le tchat</h3>

        <h4>Les messages qui gênent les joueurs dans l’utilisation du canal principal de tchat, que ce soit de manière volontaire ou involontaire, sont interdits.</h4>

        <p>Voici, entre autres, quelles peuvent être ces gênes :</br></br>
          Des lignes sans texte ou des espaces inutiles et très conséquents entre les mots</br>
          L’envoi répété du même message ou de messages similaires</br>
          L’envoi de chaque mot d’une phrase sur une nouvelle ligne</br>
          L’envoi répété de messages écrits tout en LETTRES MAJUSCULES</br>

          L’envoi de lettres qui ne forment pas de mot, qui n’ont pas de sens, par exemple : dleojhfh</br></br>
          Les messages qui sont difficiles à lire comme :</br></br>
          L’alternance de LeTtReS mAjUsCuLeS eT mInUsCuLeS</br>
          Des phrases sans espace entre les mots</br>
          Des phrases sans ou avec trop de ponctuation</br></br>

          <h3>Spam et publicité</h3>

          <h4>Les messages à caractère publicitaire et les spams gênent la fluidité du tchat et sont donc formellement interdits.</h4>

          <p>Sont considérés, entre autres, comme spams ou publicités :</br></br>
            La recommandation de sites web</br>
            Les offres d’achat, de vente ou d’échange, ainsi que les recherches correspondantes</br>
            Les escroqueries, par exemple : « J’échange mon compte, me contacter» (de tels messages seront suivis d’un bannissement immédiat et d’un blocage de compte)</br>
            L’appel à des chaînes, au boycott, à des votes ou des pétitions</br></br>
          </p>

          <h3>Contenus choquants</h3>

          <h4>N’oubliez pas que des enfants et des ados tchatent. </br>
            Aucune insulte ou provocation de joueur ne sera tolérée.</br>
            Le non-respect de ces règles peut conduire au bannissement du tchat, à l’exclusion totale du tchat et des mesures judiciaires sont également envisageables.
          </h4>

          <p>Est formellement interdit :</br></br>

            Un langage vulgaire, des expressions au contenu explicitement sexuel, et les abréviations correspondantes ou les symboles correspondants</br>
            La description d’actes ou de pratiques sexuels</br>
            La provocation, la propagande politique ou raciste et tout contenu illégal</br>
            Toute discrimination visant une religion, une minorité, tout contenu discriminant des personnes handicapées mentales ou physiques, des groupes sociaux, ou tout autre groupe de personnes</br>
            Le poste de liens menant à des pages au contenu pornographique, obscène, extrémiste ou raciste</br>
            Toute menace, harcèlement, calomnie, diffamation ou insulte de toute sorte</br></br>


            <h3>Respect du travail des modérateurs de tchat</h3>

            <p>Les modérateurs du tchat font en sorte que les règles énoncées ci-dessus soient respectées afin que le tchat soit utilisé comme moyen de communication agréable à tous.</br>
              Les modérateurs de tchat doivent être respectés des autres joueurs pour le travail qu’ils effectuent.</br>
              Les modérateurs de tchat sont bénévoles, ne bénéficient d’aucune de prime, avantage ou rétribution.</br>
              Il est interdit de communiquer les données personnelles ou les pseudonymes des modérateurs de tchat ou d’utiliser leur pseudonyme comme nom sur le tchat.</br>
              Stoppez toute discussion lorsque les modérateurs vous le demandent et respectez les règles du tchat.</br>
              C’est ainsi que les modérateurs du tchat pourront avoir le temps de venir en aide aux joueurs qui en ont besoin sans rediriger tout le monde vers le forum.</br></br>
            </p>

            <h3>Infraction aux règles du tchat</h3>

            <p>Si vous ne respectez pas les règles du tchat énoncées ci-dessus, vous pouvez être exclu du tchat.</br>
              Un avertissement peut précéder l’exclusion du tchat, le bannissement ou le blocage du compte mais cela n’est pas obligatoire.</br>
              La modération se réserve le droit de changer ou ajouter à tous moment une des règles.</br>
            </p>

